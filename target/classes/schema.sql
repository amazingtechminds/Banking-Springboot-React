CREATE SCHEMA IF NOT EXISTS "MOVIES";
SET SCHEMA "MOVIES";

--------------------------------------------------------
--  Table MOVIE_MASTER
--------------------------------------------------------
 DROP TABLE IF EXISTS "MOVIES"."MOVIE_MASTER";	
 CREATE TABLE IF NOT EXISTS "MOVIES"."MOVIE_MASTER"(
    "ID" INT AUTO_INCREMENT  PRIMARY KEY,  
	"MOVIE_ID" VARCHAR(50) NOT NULL,
	"TITLE" VARCHAR(70) NOT NULL,
	"CODE_ID" VARCHAR(50) NOT NULL
	);
	
DROP TABLE IF EXISTS "MOVIES"."MOVIE_RENTS";	
 CREATE TABLE IF NOT EXISTS "MOVIES"."MOVIE_RENTS"(
    "CODE_ID" INT AUTO_INCREMENT  PRIMARY KEY,  
	"CODE" VARCHAR(50) NOT NULL,
	"PRICE" NUMERIC NOT NULL,
	"PRICE_DAYS" INT NOT NULL,
	"RENT_FACTOR" NUMERIC NOT NULL
	);